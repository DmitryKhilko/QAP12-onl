# Задание 3. Дано целое число N (>0). Используя операции деления нацело
# и взятия остатка от деления, найти количество и сумму его цифр.
print("")
print("Задание 3")

n = 1111
sum_1 = 0
count_1 = 0

while n > 0:
    sum_1 += n % 10
    n //= 10
    count_1 += 1

print(f"Сумма цифр = {sum_1}\nКоличество цифр = {count_1}")

# Задание 4. Деду M лет, а внуку N лет. Через сколько лет дед станет вдвое
# старше внука. И сколько при этом лет будет деду и внуку
