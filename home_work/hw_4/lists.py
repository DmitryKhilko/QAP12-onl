# Задание 1. Создайте два любых списка и свяжите их с переменными.
x = ["стол", "стул", "кровать", "шкаф"]
y = ["1", "2", "3", "4"]

# Задание 2. Извлеките из первого списка второй элемент.
print(x[1])

# Задание 2. Измените во втором списке последний элемент.
# Выведите список на экран.
y[len(y) - 1] = "8"
print(y)

# Задание 3. Соедините оба списка в один, присвоив результат
# новой переменной. Выведите получившийся список на экран.
z = x + y
print(z)

# Задание 4. "Снимите" срез из соединенного списка так,
# чтобы туда попали некоторые части обоих первых списков.
# Срез свяжите с очередной новой переменной. Выведите значение
# этой переменной.

result = z[len(x) - 1:len(x) + 1]
print(result)

