# Задание 1. Дано число N. Найти произведение всех чисел от 0 до N
print("Задание 1")

n = 10
multiplication_of_numbers = 1
i = 1

while i <= n:
    multiplication_of_numbers *= i
    i += 1

print(f"Произведение всех чисел от 0 до {n} = {multiplication_of_numbers}")

